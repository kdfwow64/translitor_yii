function PopupTools(category, service) {
    var sel = document.getElementById('t' + category);
    var t_name = new Array();
    var t_index = new Array();
    t_name = tool_name[category + ' ' + service];
    t_index = tool_index[category + ' ' + service];
    if (sel && sel.options) {
        while (sel.options.length) sel.options[0] = null;
        for (var i = 0; i < t_name.length; i++) {
            if (document.createElement) {
                var newListOption = document.createElement("OPTION");
                newListOption.text = t_name[i];
                newListOption.value = t_index[i];
                if (presets[category + ' ' + service] != 0 && t_index[i] == presets[category + ' ' + service]) {
                    newListOption.selected = "true";
                }
                (sel.options.add) ? sel.options.add(newListOption) : sel.add(newListOption, null);
            }
            else {
                sel.options[i] = new Option(t_index[i], t_name[i], false, false);
            }
        }
    }
    return false;
}

